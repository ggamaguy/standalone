package web.app.pkvalve.domains;

public class EqCategory {
	private String eqCode;
	private String eqDetail;
	private String eqCapacity;
	private String eqType;

	public String getEqType() {
		return eqType;
	}
	public void setEqType(String eqType) {
		this.eqType = eqType;
	}
	public String getEqCode() {
		return eqCode;
	}
	public void setEqCode(String eqCode) {
		this.eqCode = eqCode;
	}
	public String getEqDetail() {
		return eqDetail;
	}
	public void setEqDetail(String eqDetail) {
		this.eqDetail = eqDetail;
	}
	public String getEqCapacity() {
		return eqCapacity;
	}
	public void setEqCapacity(String eqCapacity) {
		this.eqCapacity = eqCapacity;
	}
}
